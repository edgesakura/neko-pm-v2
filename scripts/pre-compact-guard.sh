#
# stdout に出力した内容がコンパクションのコンテキストに含まれ、
# サマリーに反映される。Lead の記憶を守るにゃ。

cat << 'GUARD'
【コンパクション保存指示 - 以下を必ずサマリーに含めること】

1. ご主人と合意した意思決定（全て。理由も含む）
2. 変更・作成したファイルパスと変更理由
3. 進行中タスクの状態（完了/未完了/ブロック中）
4. 次のステップとして合意した内容
5. 発見した問題点・未解決の課題
6. git commit 済みのハッシュとメッセージ
7. spawn 中の Teammate / 外部エージェントの状態
GUARD
